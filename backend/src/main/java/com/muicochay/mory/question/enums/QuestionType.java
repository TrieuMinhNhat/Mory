package com.muicochay.mory.question.enums;

public enum QuestionType {
    OPEN_ENDED,        // Free text answer
    YES_NO,            // Yes or No answer
    MULTIPLE_CHOICE,   // Choose one option from a list
    RATING,            // Numeric rating (e.g., 1-5 or 1-10)
    SCALE,             // Scale-based answer (e.g., Agree-Disagree, Likert scale)
    IMAGE,             // Answer with an image
    VIDEO,             // Answer with a video
    AUDIO              // Answer with an audio clip
}

